public class OperatorOverloading {
    public static void main(String args[]) {
        int a = 10, b = 20;
        String c = "10", d = "20";
        System.out.println(a + b);// Addition//30
        System.out.println(c + d);// concatenation//1020
        System.out.println(b - a);// binary minus//10
        System.out.println(-b);// unary minus//-20
        System.out.println((double)(27417*100/42009));
        
    }
}